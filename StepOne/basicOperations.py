'''
Lesson: Basic Operations in Python

Python is a powerful and versatile programming language that can be used for a wide range of tasks. In this lesson, we will learn about the basic operations in Python, including arithmetic, assignment, and comparison.

Arithmetic Operations

Python supports a wide range of mathematical operations, including addition (+), subtraction (-), multiplication (*), division (/), floor division (//), and modulus (%)
'''
# Addition
print(5 + 2) # 7

# Subtraction
print(5 - 2) # 3

# Multiplication
print(5 * 2) # 10

# Division
print(5 / 2) # 2.5

# Floor Division
print(5 // 2) # 2

# Modulus
print(5 % 2) # 1

'''
Assignment Operations

In Python, we use the assignment operator (=) to assign a value to a variable. Once a value is assigned to a variable, we can use that variable to perform mathematical operations.
'''

# Assignment
x = 5
y = 2

# Addition
print(x + y) # 7

# Subtraction
print(x - y) # 3

# Multiplication
print(x * y) # 10

# Division
print(x / y) # 2.5

# Floor Division
print(x // y) # 2

# Modulus
print(x % y) # 1

'''
Comparison Operations

Python also supports a wide range of comparison operators, including equal to (==), not equal to (!=), greater than (>), less than (<), greater than or equal to (>=), and less than or equal to (<=).
'''
# Equal to
print(5 == 2) # False

# Not equal to
print(5 != 2) # True

# Greater than
print(5 > 2) # True

# Less than
print(5 < 2) # False

# Greater than or equal to
print(5 >= 2) # True

# Less than or equal to
print(5 <= 2) # False


#Combining operations
#We can also use multiple operations in a single line of code.
x = 5
y = 2

# Adding and Assigning
x += y # x = x + y
print(x) # 7

# Subtracting and Assigning
x -= y # x = x - y
print(x) # 5

# Multiplying and Assigning
x *= y # x = x * y
print(x) # 10

# Dividing and Assigning
x /= y # x = x / y
print(x) # 2.5

